<template>
  <div>{{ fullName }}</div>
</template>

<script>
import { computed, toRefs } from "vue";

export default {
  props: {
    firstName: String,
    lastName: String,
  },

  setup(props, { expose }) {
    console.log(context);

    const { firstName, lastName } = toRefs(props);

    const fullName = computed(() => {
      return `${firstName.value} ${lastName.value}`;
    });

    expose({
      fullName,
    });

    return {
      fullName,
    };
  },
};
</script>
